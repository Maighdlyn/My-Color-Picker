$(document).ready(function(){

  $('.red').click(function(){
    $('body').removeClass();
    $('body').addClass('red');
  });

  $('.blue').click(function(){
    $('body').removeClass();
    $('body').addClass('blue');
  });

  $('.yellow').click(function(){
    $('body').removeClass();
    $('body').addClass('yellow');
  });

});
